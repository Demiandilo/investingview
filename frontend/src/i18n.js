/* ─── Context ────────────────────────────────────────────────────────────── */
const LangContext = createContext({ lang: "it", setLang: () => {} });

export function LangProvider({ children }) {
  const [lang, setLang] = useLocalStorage("app_lang", "it");
  return createElement(LangContext.Provider, { value: { lang, setLang } }, children);
}

export function useLang() {
  const { lang, setLang } = useContext(LangContext);
  const t = useCallback((key, params) => {
    const keys = key.split(".");
    let val = T[lang];
    for (const k of keys) { val = val?.[k]; if (val === undefined) break; }
    if (val === undefined) {
      let fallback = T["it"];
      for (const k of keys) { fallback = fallback?.[k]; if (fallback === undefined) break; }
      val = fallback ?? key;
    }
    if (typeof val === "string" && params) {
      return val.replace(/\{(\w+)\}/g, (_, k) => params[k] ?? `{${k}}`);
    }
    return val ?? key;
  }, [lang]);
  return { lang, setLang, t };
}

